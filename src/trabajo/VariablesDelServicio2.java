
package trabajo;

public class VariablesDelServicio2 {
    
    private String code, cash, name;

    public VariablesDelServicio2(String code, String cash, String name) {
        this.code = code;
        this.cash = cash;
        this.name = name;
    }

    VariablesDelServicio2() {
        
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getCash() {
        return cash;
    }

    public void setCash(String cash) {
        this.cash = cash;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }


}
